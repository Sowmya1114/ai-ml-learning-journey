# Day 4 - Lists
fruits=['apple','mango','orange']
print(fruits)
#accessing elements
print(fruits[0])
#add items
fruits.append('pineapple')
print(fruits)
#remove items
fruits.remove('pineapple')
print(fruits)

#tuples
num=(1,2,3,4,5)
print(num)
#accessing elements
print(num[1])

#sets
color={'blue','green','red'}
print(color)
#add an element
color.add('brown')
print(color)
#remove element
color.remove('brown')
print(color)
#check membership
print("red" in color)
